    private void loadTemplates() {
        List<Template> all = templateCtrl.findAll();
        DefaultListModel<Template> m = new DefaultListModel<>();
        for (Template t : all) {
            m.addElement(t);
        }
        lstTemplates.setModel(m);

        // If there are any templates, select the first one;
        // otherwise clear any selection so getSelectedValue() returns null.
        if (m.getSize() > 0) {
            lstTemplates.setSelectedIndex(0);
        } else {
            lstTemplates.clearSelection();   // equivalent to setSelectedIndex(-1)
        }

        // Update preview fields to match the (possibly empty) selection
        Template sel = lstTemplates.getSelectedValue();
        if (sel != null) {
            txtPreviewSubject.setText(sel.getSubject());
            txtPreviewBody.setText(sel.getBody());
        } else {
            txtPreviewSubject.setText("");
            txtPreviewBody.setText("");
        }
    }


