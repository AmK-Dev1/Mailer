            @Override
            public void valueChanged(ListSelectionEvent e) {
                if (!e.getValueIsAdjusting()) {
                    Template t = lstTemplates.getSelectedValue();
                    if (t != null) {
                        txtPreviewSubject.setText(t.getSubject());
                        txtPreviewBody.setText(t.getBody());
                    }
                }
            }
