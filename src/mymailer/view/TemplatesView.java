    private void btnAddTemplateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddTemplateActionPerformed
        // TODO add your handling code here:
        
        String subj = txtNewSubject.getText().trim();
            String body = txtNewBody.getText().trim();
            if (subj.isEmpty() || body.isEmpty()) {
                JOptionPane.showMessageDialog(
                  this, 
                  "Enter both subject and body", 
                  "Validation", 
                  JOptionPane.WARNING_MESSAGE
                );
                return;
            }
            Template newT = new Template();
            newT.setName(subj);        // if you use name field, or name=subj
            newT.setSubject(subj);
            newT.setBody(body);

            boolean ok = templateCtrl.addTemplate(newT);
            if (ok) {
                loadTemplates();              // refresh list
                txtNewSubject.setText("");    // clear form
                txtNewBody.setText("");
                JOptionPane.showMessageDialog(
                  this, 
                  "Template added!", 
                  "Success", 
                  JOptionPane.INFORMATION_MESSAGE
                );
            } else {
                JOptionPane.showMessageDialog(
                  this, 
                  "Failed to add template", 
                  "Error", 
                  JOptionPane.ERROR_MESSAGE
                );
            }
    }//GEN-LAST:event_btnAddTemplateActionPerformed

