    private void btnSendToGroupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendToGroupActionPerformed
        // TODO add your handling code here:
        
        Group g     = (Group) cboGroup.getSelectedItem();
        Template t2 = (Template) cboTemplateForGroup.getSelectedItem();
        btnSendToGroup.setEnabled(false);

        sendMessageCtrl.sendToGroupAsync(
          g, t2,

          // onSuccess
          () -> SwingUtilities.invokeLater(() -> {
              JOptionPane.showMessageDialog(
                this,
                "Emails sent to group “" + g.getName() + "”",
                "Success",
                JOptionPane.INFORMATION_MESSAGE
              );
              btnSendToGroup.setEnabled(true);
          }),

          // onError
          ex -> SwingUtilities.invokeLater(() -> {
              JOptionPane.showMessageDialog(
                this,
                "Failed to send to group: " + ex.getMessage(),
                "Error",
                JOptionPane.ERROR_MESSAGE
              );
              btnSendToGroup.setEnabled(true);
          })
        );
    }//GEN-LAST:event_btnSendToGroupActionPerformed

