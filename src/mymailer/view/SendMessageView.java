    private void btnSendToContactActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendToContactActionPerformed
        // TODO add your handling code here:
            Contact c  = (Contact) cboContact.getSelectedItem();
            Template t = (Template) cboTemplateForContact.getSelectedItem();
            btnSendToContact.setEnabled(false);

            sendMessageCtrl.sendToContactAsync(
              c, t,

              // onSuccess
              () -> SwingUtilities.invokeLater(() -> {
                  JOptionPane.showMessageDialog(
                    this,
                    "Email sent to " + c.getEmail(),
                    "Success",
                    JOptionPane.INFORMATION_MESSAGE
                  );
                  btnSendToContact.setEnabled(true);
              }),

              // onError
              ex -> SwingUtilities.invokeLater(() -> {
                  JOptionPane.showMessageDialog(
                    this,
                    "Failed to send: " + ex.getMessage(),
                    "Error",
                    JOptionPane.ERROR_MESSAGE
                  );
                  btnSendToContact.setEnabled(true);
              })
            );
    }//GEN-LAST:event_btnSendToContactActionPerformed

