    private void loadContacts() {
        allContacts = contactCtrl.findAll();
        ContactTableModel model = new ContactTableModel(allContacts);
        tblContacts.setModel(model);

        // re-bind the sorter so filtering & sorting still work
        sorter = new TableRowSorter<>(model);
        tblContacts.setRowSorter(sorter);
    }


    private void initSearchFilter() {
        sorter = new TableRowSorter<>((ContactTableModel)tblContacts.getModel());
        tblContacts.setRowSorter(sorter);

        txtSearch.getDocument().addDocumentListener(new DocumentListener() {
            void filter() {
                String text = txtSearch.getText();
                if (text.trim().isEmpty()) {
                    sorter.setRowFilter(null);
                } else {
                    sorter.setRowFilter(
                      RowFilter.regexFilter("(?i)" + Pattern.quote(text))
                    );
                }
            }
            @Override public void insertUpdate(DocumentEvent e) { filter(); }
            @Override public void removeUpdate(DocumentEvent e) { filter(); }
            @Override public void changedUpdate(DocumentEvent e) { filter(); }
        });
    }

