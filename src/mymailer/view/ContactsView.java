    private void addContactBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addContactBtnActionPerformed
        // TODO add your handling code here:
         // 1) Read & trim inputs
        String name  = nameInput.getText().trim();
        String email = emailInput.getText().trim();

        // 2) Simple validation
        if (name.isEmpty() || email.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "Please enter both name and email.",
                "Validation Error",
                JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (!email.matches("^.+@.+\\..+$")) {
            JOptionPane.showMessageDialog(this,
                "Please enter a valid email address.",
                "Validation Error",
                JOptionPane.WARNING_MESSAGE);
            return;
        }

        // 3) Build model object
        Contact newContact = new Contact();
        newContact.setName(name);
        newContact.setEmail(email);
        // userId will be set inside addContact()

        // 4) Delegate to controller
        boolean ok = contactCtrl.addContact(newContact);
        if (ok) {
            // 5a) Refresh table
            loadContacts();
            // 5b) Clear inputs
            nameInput.setText("");
            emailInput.setText("");
            JOptionPane.showMessageDialog(this,
              "Contact added successfully!",
              "Success",
              JOptionPane.INFORMATION_MESSAGE);
        } else {
            // 6) Show error
            JOptionPane.showMessageDialog(this,
              "Failed to add contact. Check logs for details.",
              "Error",
              JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_addContactBtnActionPerformed

