    private void loadAllGroups() {
        List<Group> groups = groupCtrl.findAll();
        lstGroups.setListData(groups.toArray(new Group[0]));
    }
    
    private void loadAllContacts() {
        List<Contact> all = contactCtrl.findAll();
        lstAllContacts.setListData(all.toArray(new Contact[0]));
    }
    
    private void loadGroupMembers(int groupId) {
        List<Contact> members = contactCtrl.findByGroup(groupId);
        lstGroupMembers.setListData(members.toArray(new Contact[0]));
    }
    
    private void initGroupSelectionListener() {
        lstGroups.addListSelectionListener(e -> {
            if (!e.getValueIsAdjusting()) {
                Group g = (Group)lstGroups.getSelectedValue();
                if (g != null) {
                    selectedGroupId = g.getId();
                    loadGroupMembers(selectedGroupId);
                }
            }
        });
    }
    
    private void initDragAndDrop() {
        // 1) allow dragging from ALL CONTACTS
        lstAllContacts.setDragEnabled(true);

        // 2) configure dropping into GROUP MEMBERS
        lstGroupMembers.setDropMode(DropMode.ON_OR_INSERT);
        lstGroupMembers.setTransferHandler(new TransferHandler() {
            @Override
            public boolean canImport(TransferSupport support) {
                return support.isDataFlavorSupported(DataFlavor.stringFlavor);
            }

            @Override
            public boolean importData(TransferSupport support) {
                if (!canImport(support) || selectedGroupId < 0) return false;
                try {
                    String contactString = (String)
                      support.getTransferable()
                             .getTransferData(DataFlavor.stringFlavor);

                    // find and persist that contact
                    for (Contact c : contactCtrl.findAll()) {
                        if (c.toString().equals(contactString)) {
                            groupCtrl.addContactToGroup(selectedGroupId, c.getId());
                            loadGroupMembers(selectedGroupId);
                            return true;
                        }
                    }
                } catch (Exception ex) {
                    ex.printStackTrace();
                }
                return false;
            }

            @Override
            protected Transferable createTransferable(JComponent comp) {
                @SuppressWarnings("unchecked")
                JList<Contact> list = (JList<Contact>) comp;
                Contact sel = list.getSelectedValue();
                return new StringSelection(sel.toString());
            }

            @Override
            public int getSourceActions(JComponent c) {
                return COPY;
            }
        });
    }

    

    
