    private void btnCreateGroupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateGroupActionPerformed
        // TODO add your handling code here:
            // 1) Read & trim inputs
       String name = txtNewGroupName.getText().trim();

       // 2) Validate
       if (name.isEmpty()) {
           JOptionPane.showMessageDialog(this,
               "Please enter a group name.",
               "Validation",
               JOptionPane.WARNING_MESSAGE);
           return;
       }

       // 3) Build your model object
       Group g = new Group();
       g.setName(name);

       // 4) Delegate to controller
       boolean ok = groupCtrl.addGroup(g);
       if (ok) {
           // 5a) Refresh the groups list
           loadAllGroups();
           // 5b) Clear the form
           txtNewGroupName.setText("");
           JOptionPane.showMessageDialog(this,
               "Group \"" + name + "\" created!",
               "Success",
               JOptionPane.INFORMATION_MESSAGE);
       } else {
           JOptionPane.showMessageDialog(this,
               "Failed to create group. Check logs for details.",
               "Error",
               JOptionPane.ERROR_MESSAGE);
       }
    }//GEN-LAST:event_btnCreateGroupActionPerformed

